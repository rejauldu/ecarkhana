<?php

namespace App\Http\Controllers\Backend\Dropdowns;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use App\Category;
use App\Dropdowns\FuelType;

class FuelTypeController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
		$fuel_types = FuelType::with('category')->get();
        return view('backend.dropdowns.fuel-types.index', compact('fuel_types'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
		$categories = Category::all();
        return view('backend.dropdowns.fuel-types.create', compact('categories'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->except('_token', '_method');
		FuelType::create($data);
		return redirect(route('fuel-types.index'))->with('message', 'Fuel Type created successfully');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
		$categories = Category::all();
        $fuel_type = FuelType::find($id);
		return view('backend.dropdowns.fuel-types.show', compact('categories', 'fuel_type'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
		$categories = Category::all();
        $fuel_type = FuelType::find($id);
		return view('backend.dropdowns.fuel-types.create', compact('categories', 'fuel_type'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $data = $request->except('_token', '_method');
		$fuel_type = FuelType::find($id);
		$fuel_type->update($data);
		
		return redirect(route('fuel-types.index'))->with('message', 'Fuel Type updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $fuel_type = FuelType::find($id);
		$fuel_type->delete();
		return redirect()->back()->with('message', 'Fuel Type has been deleted');
    }
}
